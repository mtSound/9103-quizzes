/* I want the first circle to be stationary, and for all other circles to be generated from it */
const circle = document.getElementById('myFirstCircle');

// radius FACTOR will be constant throughout
const radius = 20;

/* I found 'parseInt' in the MDN docs as a way to take the coordinates of the first circle as a string, and convert them 
back to a number so I could apply Math to them */
let x0 = parseInt(circle.getAttribute("cx"));
let y0 = parseInt(circle.getAttribute("cy"));

/* I'll randomise the colour of the original circle, but all extra circles will be an offset calculated 
from the first.
I've limited the integer to less than 255 - I found that if multiplied by 255, the first background circles could 
often start too light and the canvas appears washed out */
let r0 = Math.round(Math.random() * 180);
let g0 = Math.round(Math.random() * 180);
let b0 = Math.round(Math.random() * 180);
circle.setAttribute("fill", `rgb(${r0},${g0},${b0})`)
circle.setAttribute("r", (radius - (Math.random() * 10)))

